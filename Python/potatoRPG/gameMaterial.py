def jsonSave(data):
    file = filedialog.asksaveasfilename(
        filetypes=[("jsonファイル", "*.json")], initialdir=".\\")
    try:
        file.index(".json")
    except:
        file += ".json"
    opened = open(file, "w")
    json.dump(data, opened)
    opened.close()


def jsonGet(filename=""):
    if(filename == ""):
        filename = filedialog.askopenfilename(
            filetypes=[("jsonファイル", "*.json")], initialdir=".\\")
    opened = open(filename, "r")
    return json.load(opened)


def makeBar(canvas: tkinter.Canvas, x1, y1, x2, y2, max, value, title, mainColor):
    canvas.create_rectangle(x1, y1, x2, y2, fill="white",
                            outline="black", width=2,tag="show")
    canvas.create_rectangle(x1+(x2-x1)*(max-value)/max,
                            y1+1, x2-1, y2-1, fill=mainColor, width=0,tag="show")
    valTx = str(value)+"/"+str(max)
    text(canvas, x2+(y2-y1)/2*len(valTx)/2, y2 -
         (y2-y1)/2, valTx, "black", int((y2-y1)/1.7))
    text(canvas, x1-(y2-y1)/1.7*len(title)/2-10, y2 -
         (y2-y1)/2, title, "black", int((y2-y1)/1.7))


def text(canvas, x, y, string, colors, size,tag="show"):
    """文字列の表示"""
    canvas.create_text(x, y, text=string, fill=colors, font=(
        "Yu Gothic UI Semilight", size), tag=tag)


def shadowText(canvas, x, y, string, colorF, colorB, size):
    """影あり文字列の表示"""
    text(canvas, x+1, y+1, string, colorB, size)
    text(canvas, x, y, string, colorF, size)


